

public class ArrayBasics 
{
    public static void main(String[] args) 
    {
        int[] arr = {3,4,5,6,7};
        // Indexing
        // Accessing the element
        System.out.println(arr[0]); // Output => 3
        // Updating elements
        System.out.println(arr[2]);
        arr[2]=8;
        System.out.println(arr[2]);
    }
}
